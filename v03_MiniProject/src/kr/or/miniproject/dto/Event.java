package kr.or.miniproject.dto;

public class Event {
	private String event_code;
	private String event_id;
	private String event_title;
	private String event_cate;
	private String event_content;
	private String event_post_filecode;
	private String event_reg_date;
	
	
	public String getEvent_code() {
		return event_code;
	}
	public void setEvent_code(String event_code) {
		this.event_code = event_code;
	}
	public String getEvent_id() {
		return event_id;
	}
	public void setEvent_id(String event_id) {
		this.event_id = event_id;
	}
	public String getEvent_title() {
		return event_title;
	}
	public void setEvent_title(String event_title) {
		this.event_title = event_title;
	}
	public String getEvent_cate() {
		return event_cate;
	}
	public void setEvent_cate(String event_cate) {
		this.event_cate = event_cate;
	}
	public String getEvent_content() {
		return event_content;
	}
	public void setEvent_content(String event_content) {
		this.event_content = event_content;
	}
	public String getEvent_post_filecode() {
		return event_post_filecode;
	}
	public void setEvent_post_filecode(String event_post_filecode) {
		this.event_post_filecode = event_post_filecode;
	}
	public String getEvent_reg_date() {
		return event_reg_date;
	}
	public void setEvent_reg_date(String event_reg_date) {
		this.event_reg_date = event_reg_date;
	}
	

}
