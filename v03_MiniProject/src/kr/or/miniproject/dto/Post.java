package kr.or.miniproject.dto;

public class Post {
	private String post_code;
	private String post_id;
	private String post_title;
	private String post_cate;
	private String post_content;
	private String post_filecode;
	private String post_reg_date;
	private String post_like;
	private String post_view_num;
	public String getPost_code() {
		return post_code;
	}
	public void setPost_code(String post_code) {
		this.post_code = post_code;
	}
	public String getPost_id() {
		return post_id;
	}
	public void setPost_id(String post_id) {
		this.post_id = post_id;
	}
	public String getPost_title() {
		return post_title;
	}
	public void setPost_title(String post_title) {
		this.post_title = post_title;
	}
	public String getPost_cate() {
		return post_cate;
	}
	public void setPost_cate(String post_cate) {
		this.post_cate = post_cate;
	}
	public String getPost_content() {
		return post_content;
	}
	public void setPost_content(String post_content) {
		this.post_content = post_content;
	}
	public String getPost_filecode() {
		return post_filecode;
	}
	public void setPost_filecode(String post_filecode) {
		this.post_filecode = post_filecode;
	}
	public String getPost_reg_date() {
		return post_reg_date;
	}
	public void setPost_reg_date(String post_reg_date) {
		this.post_reg_date = post_reg_date;
	}
	public String getPost_like() {
		return post_like;
	}
	public void setPost_like(String post_like) {
		this.post_like = post_like;
	}
	public String getPost_view_num() {
		return post_view_num;
	}
	public void setPost_view_num(String post_view_num) {
		this.post_view_num = post_view_num;
	}
}
